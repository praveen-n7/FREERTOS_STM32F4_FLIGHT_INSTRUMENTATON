/* Framebuffer: 128 columns × 8 pages × 1 byte per page = 1024 bytes */
static uint8_t framebuffer[SSD1306_PAGES][SSD1306_WIDTH];
static uint8_t cursor_col = 0;
static uint8_t cursor_page = 0;

static void ssd1306_send_cmd(uint8_t cmd) {
    uint8_t buf[2] = { SSD1306_CTRL_CMD, cmd };
    i2c1_write(SSD1306_I2C_ADDR, buf, 2);
}

static void ssd1306_send_cmd2(uint8_t cmd1, uint8_t cmd2) {
    uint8_t buf[3] = { SSD1306_CTRL_CMD, cmd1, cmd2 };
    i2c1_write(SSD1306_I2C_ADDR, buf, 3);
}

void ssd1306_init(void) {
    /* Standard SSD1306 init sequence for 128x64 */
    ssd1306_send_cmd(0xAE);          /* display off */
    ssd1306_send_cmd2(0xD5, 0x80);   /* clock div/oscillator: ratio=1, freq=8 */
    ssd1306_send_cmd2(0xA8, 0x3F);   /* multiplex ratio: 64MUX */
    ssd1306_send_cmd2(0xD3, 0x00);   /* display offset: 0 */
    ssd1306_send_cmd(0x40);          /* start line: 0 */
    ssd1306_send_cmd2(0x8D, 0x14);   /* charge pump: enable */
    ssd1306_send_cmd2(0x20, 0x00);   /* memory addressing: horizontal */
    ssd1306_send_cmd(0xA1);          /* segment remap: col 127 → SEG0 */
    ssd1306_send_cmd(0xC8);          /* COM scan direction: remapped */
    ssd1306_send_cmd2(0xDA, 0x12);   /* COM pins: alternative, no remap */
    ssd1306_send_cmd2(0x81, 0xCF);   /* contrast: 207 */
    ssd1306_send_cmd2(0xD9, 0xF1);   /* pre-charge period: phase1=1, phase2=15 */
    ssd1306_send_cmd2(0xDB, 0x40);   /* VCOMH deselect level: 0.77×Vcc */
    ssd1306_send_cmd(0xA4);          /* display from RAM (not all-on) */
    ssd1306_send_cmd(0xA6);          /* normal display (not inverted) */
    ssd1306_send_cmd(0xAF);          /* display on */
    
    ssd1306_clear();
    ssd1306_update();
}

void ssd1306_clear(void) {
    memset(framebuffer, 0, sizeof(framebuffer));
    cursor_col  = 0;
    cursor_page = 0;
}

void ssd1306_set_cursor(uint8_t col, uint8_t row) {
    cursor_col  = col;
    cursor_page = row;
}

void ssd1306_print_string(const char *str) {
    while (*str) {
        char c = *str++;
        if (c < 0x20 || c > 0x7A) c = 0x20;  /* replace non-printable with space */
        
        const uint8_t *glyph = font5x7[c - 0x20];
        
        /* Write 5 columns of glyph data into framebuffer */
        for (uint8_t i = 0; i < 5; i++) {
            if (cursor_col < SSD1306_WIDTH && cursor_page < SSD1306_PAGES) {
                framebuffer[cursor_page][cursor_col] = glyph[i];
            }
            cursor_col++;
        }
        /* 1-pixel gap between characters */
        if (cursor_col < SSD1306_WIDTH) {
            framebuffer[cursor_page][cursor_col] = 0x00;
        }
        cursor_col++;
        
        /* Auto-wrap to next line */
        if (cursor_col >= SSD1306_WIDTH - 6) {
            cursor_col = 0;
            cursor_page++;
        }
    }
}

void ssd1306_update(void) {
    /* Set column and page address range to full display */
    ssd1306_send_cmd2(0x21, 0x00);   /* column start: 0 */
    ssd1306_send_cmd(0x7F);          /* column end: 127 */
    ssd1306_send_cmd2(0x22, 0x00);   /* page start: 0 */
    ssd1306_send_cmd(0x07);          /* page end: 7 */
    
    /* Send all 1024 bytes of framebuffer data */
    /* SSD1306 accepts data in one long I2C transaction with 0x40 control byte */
    /* We send page by page — each 128 bytes + 1 control byte */
    for (uint8_t page = 0; page < SSD1306_PAGES; page++) {
        /* Prepend the data control byte to each page transmission */
        /* i2c1_write with control byte inline */
        uint8_t buf[129];
        buf[0] = SSD1306_CTRL_DATA;
        memcpy(&buf[1], framebuffer[page], SSD1306_WIDTH);
        i2c1_write(SSD1306_I2C_ADDR, buf, 129);
    }
}